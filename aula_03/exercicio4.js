// Construir uma página na qual o usuário digitará 2 números e o programa exibirá, na tela, o resultado das quatro operações básicas da matemática.
let numeroUm = parseInt(prompt("Digite o primeiro número: "));
let numeroDois = parseInt(prompt("Digite o segundo número: "));
document.write(numeroUm + numeroDois);
document.write("<hr>");
document.write(numeroUm - numeroDois);
document.write("<hr>");
document.write(numeroUm * numeroDois);
document.write("<hr>");
document.write(numeroUm / numeroDois);