// Operadores Aritméticos
// 1. Adição ( + )
let soma = 5 + 3;
console.log(soma);

// 2. Subtração ( - )
let subtracao = 5 - 3;
console.log(subtracao);

// 3. Multiplicação ( * )
let multiplicacao = 5 * 3;
console.log(multiplicacao);

// 4. Divisão ( / )
let divisao = 5 / 3;
console.log(divisao);

// Operadores de Atribuição
// 1. Atribuição simples
let numero = 10;

// 2. Atribuição de adição
let numero2 = 10;
numero2 += 5; // numero2 = numero2 + 5;
console.log(numero2);

// 3. Atribuição de subtração
let numero3 = 10;
numero3 -= 5; // numero3 = numero3 - 5;
console.log(numero3);

// 4. Atribuição de multiplicação
let numero4 = 10;
numero4 *= 5; // numero4 = numero4 * 5;
console.log(numero4);

// 4. Atribuição de divisão
let numero5 = 10;
numero5 /= 5; // numero5 = numero5 / 5;
console.log(numero5);

// Operadores de Incremento e Decremento
// Incremento
let numero6 = 5;
numero6++; // numero6 = numero6 + 1
console.log(numero6);

// Decremento
let numero7 = 5;
numero7--; // numero7 = numero7 - 1
console.log(numero7);

